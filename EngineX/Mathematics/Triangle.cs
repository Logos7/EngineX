namespace EngineX.Mathematics
{
    public class Triangle
    {
        public int a;
        public int b;
        public int c;
        public Color color;

        public Triangle(int a, int b, int c, Color color)
        {
            this.a = a;
            this.b = b;
            this.c = c;
            this.color = color;
        }
    }
}
